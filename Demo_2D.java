package _pasted_code_;


public class Demo_2D
{
   public static void main(String args[])
   {
	   int marks[][]= {{50,60,70,80,90 },{81,62,65,70,70},{72,66,77,80,69}};
	   System.out.println("2D array");
	   for(int i=0;i<3;i++)
	   {
		   for(int j=0;j<5;j++)
		   {
			   System.out.print(marks[i][j]+"\t");
		   }
	      System.out.println(" ");
	   }  
	   
   }
}
